#include <unistd.h>

int	main(int arg, char **argc)
{
	int	i;
	int	j;

	if (arg > 0)
	{
		i = 1;
		while (i < arg)
		{
			j = 0;
			while (argc[i][j] != '\0')
			{
				write(1, &argc[i][j], 1);
				j++;
			}
			write (1, "\n", 1);
			i++;
		}
	}
	return (0);
}
