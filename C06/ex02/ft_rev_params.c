#include <unistd.h>

int	main(int arg, char **argv)
{
	int	i;
	int	j;

	if (arg > 0)
	{
		i = arg;
		while (i-- > 1)
		{
			j = 0;
			while (argv[i][j] != '\0')
				write(1, &argv[i][j++], 1);
			write (1, "\n", 1);
		}
	}
	return (0);
}
